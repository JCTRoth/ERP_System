-- Fix namespace-prefixed key_names and seed all frontend translation keys

-- Step 1: Fix key_name values that incorrectly include namespace prefix
-- e.g. key_name='nav.dashboard' with namespace='nav' becomes key_name='dashboard'
-- so getAllTranslations() produces nav.dashboard instead of nav.nav.dashboard
UPDATE translation_keys
SET key_name = SUBSTRING(key_name FROM LENGTH(namespace) + 2),
    updated_at = NOW()
WHERE key_name LIKE namespace || '.' || '%'
  AND key_name != namespace;

