-- Ensure translation_keys exist for all seeded keys
INSERT INTO translation_keys (key_name, namespace, description)
SELECT DISTINCT
  substring(full_key from position('.' in full_key) + 1) AS key_name,
  split_part(full_key, '.', 1) AS namespace,
  MIN(CASE WHEN language = 'en' THEN value_text ELSE NULL END) AS description
FROM tmp_locale_seed
WHERE position('.' in full_key) > 0
GROUP BY full_key
ON CONFLICT (key_name, namespace) DO NOTHING;

-- Update existing default translation values
UPDATE translation_values tv
SET value_text = s.value_text,
    updated_at = NOW()
FROM (
  SELECT
    split_part(t.full_key, '.', 1) AS namespace,
    substring(t.full_key from position('.' in t.full_key) + 1) AS key_name,
    t.language,
    t.value_text
  FROM tmp_locale_seed t
) s
JOIN translation_keys tk
  ON tk.namespace = s.namespace
 AND tk.key_name = s.key_name
WHERE tv.key_id = tk.id
  AND tv.language = s.language
  AND tv.company_id IS NULL;

